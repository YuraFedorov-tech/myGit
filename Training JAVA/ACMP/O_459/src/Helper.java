
public class Helper {

	public static void pr(int[][] a, int m, int n) {
		for(int i=0;i<m;i++){
			for(int j=0;j<n;j++){
				System.out.print(a[i][j]+ "\t");
			}
			System.out.println();
		}
		System.out.println();
	}

	public static void pr(String[][] b, int m, int n) {
		for(int i=0;i<m;i++){
			for(int j=0;j<n;j++){
				System.out.print(b[i][j]+ "\t");
			}
			System.out.println();
		}
		System.out.println();
		
	}
	}


